void  _Device_Commun_MUX_Init(void){
    //set as  output  pins
    GPIO_setAsOutputPin( COMMUNICATION_MUX_PORT,COMMUNICATION_MUX_A0_PIN );
    GPIO_setOutputLowOnPin( COMMUNICATION_MUX_PORT, COMMUNICATION_MUX_A0_PIN );
}

void _Device_Set_Commun_Mux_Channel(Communication_Mux_Channels channel){

    switch(channel){
        case Comm_UART_Mux_Ch:
            GPIO_setOutputHighOnPin( COMMUNICATION_MUX_PORT, COMMUNICATION_MUX_A0_PIN );
            break;
        case Comm_RS485_Mux_Ch:
        default:
            GPIO_setOutputLowOnPin( COMMUNICATION_MUX_PORT, COMMUNICATION_MUX_A0_PIN );
            break;
    }
}



